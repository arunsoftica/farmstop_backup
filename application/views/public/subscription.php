    $(document).ready(function(){
 let scroll_link = $('.scroll');

  //smooth scrolling -----------------------
  scroll_link.click(function(e){
      e.preventDefault();
      let url = $('body').find($(this).attr('href')).offset().top;
      $('html, body').animate({
        scrollTop : url
      },700);
      $(this).parent().addClass('active');
      $(this).parent().siblings().removeClass('active');
      return false;	
   });
   
   $(document).on('click','.mpopup', function (e){
                var vid = $(this).attr('value');
                $('#attribute_id').val(vid);
             
              
                  $.ajax({
                  url: "<?php echo base_url('Ajaxcontroller/getVariationDetail') ?>",
                  type: "GET",           
                  data: {vid:vid},
                  success: function(data){
                      //alert(data);
                      $('#xid').html('');
                      $('#xid').html(data);
                      $('#submit').click();
                    
                   }
                  });
              
           });
           
           $('#pomsg').html('');
           
           $(document).on('submit','.add_cart',(function(e){
        
        $('#response_edit').html(" ");
        
        $("#submit").attr('disabled','true');
        //$("#submit").text('please wait ...);
        e.preventDefault();
          $.ajax({
          url: "<?php echo base_url('Ajaxcontroller/add_item_to_cart') ?>",
          type: "POST",        
          data: new FormData(this),
          contentType: false,
          cache: false,
          processData:false,  
          success: function(data){
            
            
            $("#submit").removeAttr('disabled');
            //$(".add_cart")[qq].reset();
            $('.add_cart').trigger("reset");
            
            
            
            var cart = 1;
                                    
            $.ajax({
                type: "GET",
                
                url: "<?php echo base_url("Ajaxcontroller/add_item_to_cart") ?>",
                data: {cart:cart},
                success: function (data) {
                    
                    $('#items').empty();
                    $('#items').append(data);
                    $('#itemz').html('');
                    $('#itemz').html(data);
                    
                       window.location.href="<?php echo base_url('checkout') ?>";
                     
                }
            });
    
         }  
         });  
    }));
   
});
